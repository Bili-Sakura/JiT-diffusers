import argparse
import json
import os
import sys
from pathlib import Path

import torch

sys.path.append(str(Path(__file__).resolve().parents[1]))
from jit_diffusers import JiTDiffusersModel


def get_args():
    parser = argparse.ArgumentParser(description="Convert diffusers JiT model back to JiT checkpoint format.")
    parser.add_argument("--model_path", type=str, required=True, help="Path to diffusers model directory.")
    parser.add_argument("--output_path", type=str, required=True, help="Output JiT checkpoint path (.pth).")
    parser.add_argument(
        "--ema_mode",
        type=str,
        default="copy_to_both",
        choices=["none", "copy_to_both"],
        help="How to reconstruct EMA states in JiT checkpoint.",
    )
    parser.add_argument(
        "--epoch",
        type=int,
        default=0,
        help="Epoch value to store in the output checkpoint.",
    )
    parser.add_argument(
        "--metadata_path",
        type=str,
        default=None,
        help="Optional conversion_metadata.json generated by convert_jit_to_diffusers.py.",
    )
    return parser.parse_args()


def main():
    args = get_args()

    model = JiTDiffusersModel.from_pretrained(args.model_path)
    checkpoint = model.to_jit_checkpoint(ema_mode=args.ema_mode)
    checkpoint["epoch"] = args.epoch
    checkpoint["optimizer"] = {}

    metadata_path = args.metadata_path or os.path.join(args.model_path, "conversion_metadata.json")
    metadata_args = None
    if os.path.exists(metadata_path):
        with open(metadata_path, "r", encoding="utf-8") as f:
            metadata = json.load(f)
        metadata_args = metadata.get("jit_args")

    source_args = metadata_args or {}
    model_type = (
        source_args.get("model")
        or source_args.get("model_name")
        or source_args.get("model_type")
        or getattr(model.config, "model_type", getattr(model.config, "model_name"))
    )
    sample_size = (
        source_args.get("img_size")
        or source_args.get("image_size")
        or source_args.get("sample_size")
        or getattr(model.config, "sample_size", getattr(model.config, "image_size"))
    )
    num_class_embeds = (
        source_args.get("class_num")
        or source_args.get("num_classes")
        or source_args.get("num_class_embeds")
        or getattr(model.config, "num_class_embeds", getattr(model.config, "num_classes"))
    )
    attention_dropout = (
        source_args.get("attn_dropout")
        or source_args.get("attention_dropout")
        or getattr(model.config, "attention_dropout", getattr(model.config, "attn_dropout", 0.0))
    )
    dropout = (
        source_args.get("proj_dropout")
        or source_args.get("dropout")
        or getattr(model.config, "dropout", getattr(model.config, "proj_dropout", 0.0))
    )

    args_dict = {
        "model": model_type,
        "img_size": sample_size,
        "class_num": num_class_embeds,
        "attn_dropout": attention_dropout,
        "proj_dropout": dropout,
        "model_type": model_type,
        "sample_size": sample_size,
        "num_class_embeds": num_class_embeds,
        "attention_dropout": attention_dropout,
        "dropout": dropout,
    }
    checkpoint["args"] = argparse.Namespace(**args_dict)

    output_dir = os.path.dirname(os.path.abspath(args.output_path))
    if output_dir:
        os.makedirs(output_dir, exist_ok=True)
    torch.save(checkpoint, args.output_path)
    print(f"Saved JiT checkpoint to: {args.output_path}")


if __name__ == "__main__":
    main()
